        )
    )

    // –î–ª—è –æ–±—Ä–∞—Ç–Ω–æ–π —Å–æ–≤–º–µ—Å—Ç–∏–º–æ—Å—Ç–∏ - —Ç–æ–ª—å–∫–æ –∫–∞–∑–∞—Ö—Å—Ç–∞–Ω—Å–∫–∏–µ –∞—ç—Ä–æ–ø–æ—Ä—Ç—ã
    val KZ_AIRPORTS: Map<String, String> = COUNTRIES.first { it.code == "KZ" }.airports

    val ICAO_LIST: List<String> = KZ_AIRPORTS.keys.toList()

    /**
     * –ü–æ–ª—É—á–∏—Ç—å –Ω–∞–∑–≤–∞–Ω–∏–µ –≥–æ—Ä–æ–¥–∞ –ø–æ ICAO –∫–æ–¥—É (–∏—â–µ—Ç –ø–æ –≤—Å–µ–º —Å—Ç—Ä–∞–Ω–∞–º)
     */
    fun getCityName(icao: String): String {
        val upperIcao = icao.uppercase()
        for (country in COUNTRIES) {
            country.airports[upperIcao]?.let { return it }
        }
        return icao
    }

    /**
     * –ü–æ–ª—É—á–∏—Ç—å —Å—Ç—Ä–∞–Ω—É –ø–æ ICAO –∫–æ–¥—É –∞—ç—Ä–æ–ø–æ—Ä—Ç–∞
     */
    fun getCountryByIcao(icao: String): Country? {
        val upperIcao = icao.uppercase()
        return COUNTRIES.find { it.airports.containsKey(upperIcao) }
    }

    /**
     * –ü–æ–ª—É—á–∏—Ç—å —Å–ø–∏—Å–æ–∫ ICAO –∫–æ–¥–æ–≤ –¥–ª—è –≤—ã–±—Ä–∞–Ω–Ω—ã—Ö —Å—Ç—Ä–∞–Ω
     */
    fun getIcaoListForCountries(countryCodes: Set<String>): List<String> {
        return COUNTRIES
            .filter { it.code in countryCodes }
            .flatMap { it.airports.keys }
    }

    /**
     * –ü–æ–ª—É—á–∏—Ç—å –≤—Å–µ —Å—Ç—Ä–∞–Ω—ã
     */
    fun getAllCountries(): List<Country> = COUNTRIES
